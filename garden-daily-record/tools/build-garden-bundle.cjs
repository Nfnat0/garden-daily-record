const fs = require('node:fs');
const https = require('node:https');
const path = require('node:path');
const vm = require('node:vm');

const ROOT = path.resolve(__dirname, '..');
const BABEL_URL = 'https://unpkg.com/@babel/standalone@7.29.0/babel.min.js';
const SOURCES = [
  'tweaks-panel.jsx',
  'data.jsx',
  'plant.jsx',
  'garden-view.jsx',
  'screen-plan.jsx',
  'screen-today.jsx',
  'screen-dashboard.jsx',
  'screens-other.jsx',
  'app.jsx',
];

function download(url, redirects = 0) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if ([301, 302, 303, 307, 308].includes(res.statusCode) && res.headers.location) {
        if (redirects > 5) {
          reject(new Error(`Too many redirects while fetching ${url}`));
          return;
        }
        download(new URL(res.headers.location, url).toString(), redirects + 1).then(resolve, reject);
        return;
      }
      if (res.statusCode !== 200) {
        reject(new Error(`Failed to fetch ${url}: HTTP ${res.statusCode}`));
        return;
      }
      res.setEncoding('utf8');
      let body = '';
      res.on('data', (chunk) => {
        body += chunk;
      });
      res.on('end', () => resolve(body));
    }).on('error', reject);
  });
}

async function loadBabel() {
  const source = await download(BABEL_URL);
  const context = { window: {}, self: {}, console };
  context.globalThis = context;
  vm.createContext(context);
  vm.runInContext(source, context, { filename: 'babel-standalone.js' });
  return context.Babel || context.window.Babel || context.self.Babel;
}

async function main() {
  const Babel = await loadBabel();
  if (!Babel) throw new Error('Babel standalone did not initialize.');

  const chunks = SOURCES.map((file) => {
    const source = fs.readFileSync(path.join(ROOT, file), 'utf8');
    const result = Babel.transform(source, {
      filename: file,
      sourceType: 'script',
      presets: [['react', { runtime: 'classic' }]],
      comments: true,
      compact: false,
    });
    return `// ---- ${file} ----\n${result.code}\n`;
  });

  const banner = [
    '/*',
    ' * Generated by tools/build-garden-bundle.cjs.',
    ' * Edit the .jsx sources, then regenerate this file.',
    ' */',
    '',
  ].join('\n');
  fs.writeFileSync(path.join(ROOT, 'garden-app.bundle.js'), `${banner}${chunks.join('\n')}`, 'utf8');
  console.log(`Built garden-app.bundle.js from ${SOURCES.length} files.`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
